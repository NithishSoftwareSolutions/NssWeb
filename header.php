<script>
    document.addEventListener('DOMContentLoaded', () => {
        const currentPageURL = window.location.href;

        const links = [
            { id: 'home-link', urlPart: 'index' },
            { id: 'tech-link', urlPart: 'technologies' },
            { id: 'our-people-link', urlPart: 'our_people' },
            { id: 'sol-link', urlPart: 'solutions' },
            { id: 'intern-link', urlPart: 'job' },
            { id: 'contact-link', urlPart: 'contact' }
        ];

        links.forEach(link => {
            if (currentPageURL.includes(link.urlPart)) {
                document.getElementById(link.id).classList.add('active');
            }
        });

        // Loader functionality
        const loader = document.getElementById('loader');
        const navLinks = document.querySelectorAll('.nav-link');

        navLinks.forEach(link => {
            link.addEventListener('click', () => {
                loader.style.display = 'block';
                navLinks.forEach(navLink => navLink.classList.remove('active'));
                link.classList.add('active');
            });
        });

        window.addEventListener('load', () => {
            loader.style.display = 'none';
        });
    });
</script>